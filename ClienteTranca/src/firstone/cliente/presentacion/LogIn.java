    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        Guardia guardia = guardiaNegocio.autenticarGuardia(jtext_ci.getText(), jtext_contrasenia.getText());
        if (guardia != null)
        {
            JFrame.setDefaultLookAndFeelDecorated(true);
            Main frame = new Main(guardia);
            frame.setTitle("IdentiFour - Sistema de Control de Acceso Vehicular");
            ImageIcon img = new ImageIcon("etc/logo.png");
            frame.setIconImage(img.getImage());
            frame.setDefaultCloseOperation(JFrame.DISPOSE_ON_CLOSE);
            frame.pack();
            frame.setVisible(true);
            this.dispose();
        }else
            JOptionPane.showMessageDialog(rootPane, "Carnet de Identidad o Contrase√±a incorrectos", "Ingresando a IdentiFour", JOptionPane.WARNING_MESSAGE);
    }//GEN-LAST:event_jButton1ActionPerformed

